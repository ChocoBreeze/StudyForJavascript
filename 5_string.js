// <5> 기본적인 문자열 메서드
// indexOf() : 지정된 값이 처음 나타나는 위치 반환
// slice() : 문자열의 지정된 부분을 새 문자열로 반환
// toUpperCase() : 문자열 내의 모든 문자를 대문자로 바꿈
// toLowerCase() : 문자열 내의 모든 문자를 소문자로 바꿈
// startsWith() : 매개변수로 받은 문자열로 문자열이 시작하는지 확인
// endsWith() : 매개변수로 받은 문자열로 문자열이 끝나는지 확인
// includes() : 포함 여부 확인
// repeat() : 횟수만큼 반복